SJ_DATA.getSuggested = function(p) {
  var available = SJ_DATA.pilgrimages.filter(function(x) {
    return x.id !== p.id && x.status !== 'cancelado' && x.status !== 'esgotada';
  });
  if (available.length === 0) return null;
  var destWords = (p.destination || '').toLowerCase().split(/[\s·,\/]+/).filter(function(w){ return w.length > 3; });
  var sameRegion = available.filter(function(x) {
    var xd = (x.destination || '').toLowerCase();
    return destWords.some(function(w){ return xd.indexOf(w) >= 0; });
  });
  if (sameRegion.length > 0) {
    sameRegion.sort(function(a, b) {
      var da = SJ_DATA.getDate(a), db = SJ_DATA.getDate(b);
      if (da && db) return da - db; if (da) return -1; if (db) return 1; return 0;
    });
    return sameRegion[0];
  }
  var dated = available.filter(function(x) { var d = SJ_DATA.getDate(x); return d && d >= new Date(); });
  dated.sort(function(a, b) { return SJ_DATA.getDate(a) - SJ_DATA.getDate(b); });
  return dated.length > 0 ? dated[0] : available[0];
};

